    private void BtAlteraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtAlteraActionPerformed
        // TODO add your handling code here:
        if (jTabAluno.getSelectedRow() != -1){
            
        ClassAluno a = new ClassAluno();
        AlunoDAO dao = new AlunoDAO();
        a.setNome(TxtFieldNome.getText());
        a.setCodigo((int) jTabAluno.getValueAt(jTabAluno.getSelectedRow(), 0));
        dao.update(a);
        readTable();
        limpaCampos();
        
        }
    }//GEN-LAST:event_BtAlteraActionPerformed

