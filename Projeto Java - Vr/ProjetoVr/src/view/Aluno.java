        public void readTable(){
    
        DefaultTableModel modelo = (DefaultTableModel) jTabAluno.getModel();
        AlunoDAO adao = new AlunoDAO();
        modelo.setRowCount(0);
        
        for (ClassAluno a : adao.read()) {
            
             modelo.addRow(new Object[]{
                 a.getCodigo(),
                 a.getNome()
             }
             ); 
            
        }
    
    }
