    public void readAluno(){
    
        DefaultTableModel modelo = (DefaultTableModel) jTabAluno.getModel();
        AlunoDAO adao = new AlunoDAO();
        modelo.setRowCount(0);
        
        for (ClassAluno a : adao.read()) {
            
                modelo.addRow(new Object[]{
                     a.getCodigo(),
                     a.getNome()
                }
            ); 
            
        }
    
    }
    
    public void readCurso(){
    
        if (jTabAluno.getSelectedRow() != -1){
            
            ClassAluno a = new ClassAluno();
            CursoDAO dao = new CursoDAO();
            a.setCodigo((int) jTabAluno.getValueAt(jTabAluno.getSelectedRow(), 0));

            DefaultTableModel modeloC = (DefaultTableModel) jTabCurso.getModel();
            modeloC.setRowCount(0);

            for (ClassCurso c : dao.readDisp(a)) {

                 modeloC.addRow(new Object[]{
                     c.getCodigo(),
                     c.getDescricao(),
                     c.getEmenta()
                 }
                 ); 

            }
        
        }  
    
    }
    
    public void readMat(){
    
         if (jTabAluno.getSelectedRow() != -1){
            
            ClassAluno a = new ClassAluno();
            MatriculaDAO dao = new MatriculaDAO();
            a.setCodigo((int) jTabAluno.getValueAt(jTabAluno.getSelectedRow(), 0));

            DefaultTableModel modeloM = (DefaultTableModel) jTabMat.getModel();
            modeloM.setRowCount(0);

            for (ClassMatricula m : dao.read(a)) {

                 modeloM.addRow(new Object[]{
                     
                     m.getCodigo(),
                     m.getCodigo_curso(),
                     m.getDescricao(),
                     m.getEmenta()
                 }
                 ); 

            }
        
        }  
    
    }

